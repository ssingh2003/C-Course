#include<iostream>
using namespace std;
int main(){


    cout<<"Int size is:"<<sizeof(int)<<endl;
    cout<<"Char size is:"<<sizeof(char)<<endl;
    cout<<"Float size is:"<<sizeof(float)<<endl;
    cout<<"short int is:"<<sizeof(short int)<<endl;
    cout<<"unsigned long int is"<<sizeof(long int)<<endl;
    cout<<"signed long long int is:"<<sizeof(long long int)<<endl;
    return 0;
}